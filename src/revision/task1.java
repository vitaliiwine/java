package revision;

/*
Создаем символьный массив, содержащий английский алфавит
 */

public class task1 {
    public static void main(String[] args) {
        char[] alphabet = "abcdefghijklmnopqrstuvwqyz".toCharArray();

        for (int i = 0; i < alphabet.length; i++) {
            System.out.println(alphabet[i]);
        }
    }
}
